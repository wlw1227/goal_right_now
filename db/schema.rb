  enable_extension "plpgsql"

  create_table "habits", force: :cascade do |t|
    t.string "description"
    t.datetime "created_at", precision: 6, null: false
    t.datetime "updated_at", precision: 6, null: false
    t.integer "days"
    t.integer "user_id"
